func (ctrl *daemonSetController) DeployDaemonSet(ctx *gin.Context) {
	var result ResponseDTO
	payload := new(appsv1.DaemonSet)
	if err := ctx.Bind(payload); err != nil {
		log.Logger.Errorw("Failed to bind deploy daemonSet payload", "err", err.Error())
		SendErrorResponse(ctx, err.Error())
		return
	}

	clusterGroup := ctx.Query("cluster_id")
	if clusterGroup == "" {
		log.Logger.Errorw("Cluster id required in query params", "value", clusterGroup)
		SendErrorResponse(ctx, "Cluster id required in query params")
		return
	}

	input := new(k8s.DeployDaemonSetInputParams)
	input.DaemonSet = payload
	if input.DaemonSet.Namespace == "" {
		log.Logger.Errorw("namespace required in payload", "value", "daemonSet deploy")
		SendErrorResponse(ctx, ErrNamespaceEmpty)
		return
	}
	taskName := tasks.GetTaskName(k8s.DaemonSetService().DeployDaemonSet)
	logRequestedTaskController("daemonSet", taskName)
	inputTask, err := json.Marshal(input)
	if err != nil {
		logErrMarshalTaskController(taskName, err)
	}
	res, err := worker.TaskToAgent().SendToWorker(ctx, taskName, inputTask, clusterGroup)
	if err != nil {
		SendErrorResponse(ctx, err.Error())
		return
	}
	err = json.Unmarshal([]byte(res.Output), &result)
	if err != nil {
		SendErrorResponse(ctx, err.Error())
		return
	}
	SendResponse(ctx, result)
}

